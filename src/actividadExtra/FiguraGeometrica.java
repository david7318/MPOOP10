package actividadExtra;
/**
 * Interfaz para una FiguraGeometrica
 * @author David Gomez, David Montoya
 */
public interface FiguraGeometrica {
    /**
     * Método de la interfaz que dibuja una FiguraGeometrica
     */
    void dibujarse();
    /**
     * Método de la interfaz que mueve una FiguraGeometrica
     */
    void moverse();
    /**
     * Método de la interfaz que borra una FiguraGeometrica
     */
    void borrarse();
    /**
     * Método de la interfaz que crea una FiguraGeometrica
     */
    void crearse();
}
